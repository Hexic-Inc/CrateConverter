package US.Siiant.Main.CrateReloaded;

public class Effect {

    private String classType;
    private String category;
    private String radius;

    private String fireworkType;
    private String color;
    private String color2;
    private String color3;
    private String fadeColor;
    private Boolean trail;
    private int power;

    private String particle;
    private int particles;
    private int period;

    public String getClassType() {
        return classType;
    }

    public void setClassType(String classType) {
        this.classType = classType;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getRadius() {
        return radius;
    }

    public void setRadius(String radius) {
        this.radius = radius;
    }

    public String getFireworkType() {
        return fireworkType;
    }

    public void setFireworkType(String fireworkType) {
        this.fireworkType = fireworkType;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor2() {
        return color2;
    }

    public void setColor2(String color2) {
        this.color2 = color2;
    }

    public String getColor3() {
        return color3;
    }

    public void setColor3(String color3) {
        this.color3 = color3;
    }

    public String getFadeColor() {
        return fadeColor;
    }

    public void setFadeColor(String fadeColor) {
        this.fadeColor = fadeColor;
    }

    public boolean getTrail() {
        return trail;
    }

    public void setTrail(boolean trail) {
        this.trail = trail;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }

    public String getParticle() {
        return particle;
    }

    public void setParticle(String particle) {
        this.particle = particle;
    }

    public int getParticles() {
        return particles;
    }

    public void setParticles(int particles) {
        this.particles = particles;
    }

    public int getPeriod() {
        return period;
    }

    public void setPeriod(int period) {
        this.period = period;
    }
}
